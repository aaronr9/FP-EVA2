public class EVA2_1_CICLOS {

    public static void main(String[] args) {
        System.out.println("Hello World!");
        //REPETICION DE INTRUCCIONES:
        //CICLOS O BUCLES
        //CICLO FOR(desde)
        
        for(int i = 1; i <= 10; i++){
            //++--> operador para sumar 1 unidad
            //-- --> resta una unidad
            //+= suma N valor
            //-= resta N valor
            //*=Multiplicar por N
            // /= divide entre N
            System.out.print(i+"-");
           }
        System.out.println("");
        for(int i = 10; i >= 1; i--){
            System.out.print(i+"-");
        }
        
       
                
    }          
}
